class Solution{
  public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        // code here
        // return the head of intersection list
        unordered_map<int , int> m;
        Node* newHead = NULL;
        Node* realHead;
        Node* temp = head2;
        while(temp!=NULL){
            m[temp->data]++;
            temp=temp->next;
        }
        Node* temp1 = head1;
        // Node* prev;
        while(temp1!=NULL){
            if(m.find(temp1->data)!=m.end()){
                if(newHead ==NULL){
                    newHead = temp1;
                    realHead = temp1;
                }
                else{
                    newHead->next = temp1;
                    newHead = temp1;
                }
            }
            
            temp1 = temp1->next;
        }
        newHead->next =NULL;
        return realHead;
    }
};

